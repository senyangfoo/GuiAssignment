package domain;

import jakarta.persistence.Basic;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Id;
import jakarta.persistence.NamedQueries;
import jakarta.persistence.NamedQuery;
import jakarta.persistence.Table;
import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Size;
import jakarta.xml.bind.annotation.XmlRootElement;
import java.io.Serializable;

/**
 *
 * @author Abcong
 */
@Entity
@Table(name = "ADMIN")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Admin.findAll", query = "SELECT a FROM Admin a"),
    @NamedQuery(name = "Admin.findByEmpId", query = "SELECT a FROM Admin a WHERE a.empId = :empId"),
    @NamedQuery(name = "Admin.findByEmpName", query = "SELECT a FROM Admin a WHERE a.empName = :empName"),
    @NamedQuery(name = "Admin.findByEmpEmail", query = "SELECT a FROM Admin a WHERE a.empEmail = :empEmail"),
    @NamedQuery(name = "Admin.findByEmpPassword", query = "SELECT a FROM Admin a WHERE a.empPassword = :empPassword")})
public class Admin implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 7)
    @Column(name = "EMP_ID")
    private String empId;
    @Size(max = 80)
    @Column(name = "EMP_NAME")
    private String empName;
    @Size(max = 80)
    @Column(name = "EMP_EMAIL")
    private String empEmail;
    @Size(max = 20)
    @Column(name = "EMP_PASSWORD")
    private String empPassword;

    public Admin() {
    }

    public Admin(String empId) {
        this.empId = empId;
    }

    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpEmail() {
        return empEmail;
    }

    public void setEmpEmail(String empEmail) {
        this.empEmail = empEmail;
    }

    public String getEmpPassword() {
        return empPassword;
    }

    public void setEmpPassword(String empPassword) {
        this.empPassword = empPassword;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (empId != null ? empId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Admin)) {
            return false;
        }
        Admin other = (Admin) object;
        if ((this.empId == null && other.empId != null) || (this.empId != null && !this.empId.equals(other.empId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "domain.Admin[ empId=" + empId + " ]";
    }
    
}
